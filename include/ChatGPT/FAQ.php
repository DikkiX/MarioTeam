Wat is Ouderlijk toezicht?
Ouderlijk toezicht is een app gemaakt door Nintendo voor ouders die meer inzicht willen over het gamegedrag van hun kinderen op de Nintendo Switch.

Bestaat het Ouderlijk toezicht van Nintendo al lang?
Ouderlijk toezicht werd op Nintendo systemen voor het eerst gebruikt op de Nintendo DS. Vanaf de Wii had elk Nintendo Systeem de mogelijkheid om met het internet te verbinden. Daarom werd er bij die systemen ook extra functies toegevoegd aan het ouderlijk toezicht. De basisfuncties van ouderlijk toezicht zijn de leeftijdsrestrictie en het moeten invoeren van een code voordat je een game kan kopen of downloaden. Op de 3DS en Wii U kwamen er meer functies bij zoals het limiteren van het 3D effect op de 3DS en online functionaliteiten op de Wii U.

Wat kan ik met Ouderlijk toezicht op de Switch?
De top 3 belangrijkste functies van ouderlijk toezicht zijn:
Weten of je kinderen teveel gamen.
Zien wat voor games ze spelen.
Controleren of de spellen die ze spelen wel geschikt zijn voor hun leeftijd.


Met ouderlijk toezicht kan je precies instellen welke spellen je kinderen mogen spelen en hoe lang ze die mogen spelen. Je kan ook instellen of ze spellen zoals Mario Kart of Splatoon online mogen spelen, wie ze toevoegen als vriend en het gebruik van social media, zoals het posten van screenshots naar bijvoorbeeld Facebook of Twitter.

Veel ouders hebben interesse in welke games hun kinderen spelen, maar weten niet zo goed hoe ze dat gesprek aan kunnen gaan. Ouderlijk toezicht kan daarbij ook helpen; je kan in de app namelijk precies zien welke game ze het leukst vinden.

Zal ik het Ouderlijk toezicht gaan gebruiken op de Switch?
Wanneer je bang bent dat je kinderen te veel achter een spelcomputer zitten kan het handig zijn om het ouderlijk toezicht te gaan gebruiken. Ook kan het handig zijn om het gesprek aan te gaan met je kinderen door te weten wat ze allemaal doen op de Switch.

Je kunt het ouderlijk toezicht niet gebruiken om mee te lezen wat je kinderen precies online zeggen en doen, je kunt wel instellen dat kinderen niet online kunnen.

Hoe krijg ik het ouderlijk toezicht?
De makkelijkste manier om het ouderlijk toezicht in te stellen is om de app te downloaden op je mobiele telefoon. De app is beschikbaar in de Apple App Store en de Google Play Store. Mocht je geen gebruik willen/kunnen maken van de app kan je het ook instellen via de instellingen van de Switch.



Help! Mijn N64 of SNES gaat niet aan, wat nu?
Als een Nintendo 64/SNES het niet meteen doet na ontvangst is dat natuurlijk heel vervelend. Gelukkig is dit probleem vaak snel op te lossen met ons trucje:
1. Maak alle kabels los van de Nintendo 64/SNES.
2. Blaas in alle ingangen van de Nintendo 64/SNES.
3. Sluit alle kabels weer aan.
4. Controleer of er een blokje onder het klepje aan de bovenkant zit.
5. Blaas in de ingang van de Nintendo 64/SNES en in de gamepack.
6. Druk een spel in de Nintendo 64/SNES.
7. Til de gamepack weer 1-3 mm omhoog.
8. Zet de Nintendo 64/SNES aan en wacht 5 seconde.

Werkt dit niet? Kijk dan naar het lampje bovenop de Nintendo 64/SNES.
- Geen rood lampje? Controleer dan of de voeding goed is aangesloten. De Nintendo 64/SNES krijgt namelijk geen stroom, waardoor deze niet aangaat.
- Een rood knipperent lampje? Er is helaas iets stuk in de Nintendo 64/SNES. We kunnen hem voor je omruilen.
- Wel een rood lampje? De Nintendo 64/SNES krijgt in ieder geval stroom. Probeer ons trucje eens met een ander spel. Kun je proberen de Nintendo 64/SNES aan te sluiten op een andere tv? Mocht hij het daar wel doen, dan staan de instellingen van de eerste tv waarschijnlijk niet goed (kies de AV ingang). Probeer eens van kanaal te wisselen of de Nintendo 64/SNES op een andere manier aan te sluiten (met of zonder scartblokje bijvoorbeeld).

Mijn controller reageert niet goed
In de handleiding van Nintendo 64 spellen staat extra informatie over de Nintendo 64 Controller. Zij geven de volgende tips:
Raak de Analoge Stick (joy stick) niet aan bij het aanzetten van het Control Deck.
Als de stick verkeerd is ingesteld, kun je deze standaardinstelling weer ongedaan maken. Dit kun je doen door de stick in de standaardpositie te zetten en dan met ingedrukte L- en R-knoppen op de Start Knop drukken.
Hoe kan ik spelgegevens wissen?
De gegevens van de meeste spellen kun je zelf gemakkelijk wissen in het file-selectie menu of gamemenu. Bij options kun je kiezen voor de optie "all data clear".


Wat is de Nintendo Switch?
De Nintendo Switch is een spelcomputer die de Nintendo Wii U en de Nintendo 3DS opvolgd. In tegenstelling tot de Wii U en de 3DS is de Switch geen standaard homeconsole of handheld console, maar een combinatie daarvan.


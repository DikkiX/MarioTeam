Schoonmaken? Onze ervaring is dat schoonmaken van de gamepack contactpunten alleen zin heeft wanneer het vies is. Contactpuntjes schoonmaken (met alcohol bijvoorbeeld) heeft weinig nut als het er mooi uit ziet.

Contactspray / WD-40? Contact- en reinigings-spray werkt erg goed wanneer je moeite hebt met het opstarten van meerdere games. De spelcomputer is wat ouder en kan wel wat onderhoud gebruiken. Spuit zo min mogelijk spray op de tandjes. Kies een game waar je niet veel waarde aan hecht, want deze kan een beetje vettig gaan worden. Doe de game meerdere keren in de console en haal deze er weer uit. Zo wordt de contact spray goed verdeeld over de tandjes.

